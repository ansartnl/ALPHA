CREATE TABLE SENT_AFTN 
(
  ID NUMBER NOT NULL 
, CREATED TIMESTAMP NOT NULL 
, TEXT VARCHAR2(4000 CHAR) 
, HEADER VARCHAR2(4000 CHAR) 
, MESSAGE VARCHAR2(4000 CHAR) 
, ADDRESSES VARCHAR2(4000 CHAR) 
, CONSTRAINT SENT_AFTN_PK PRIMARY KEY 
  (
    ID 
  )
  ENABLE 
);

CREATE SEQUENCE  "AERO"."SENT_AFTN_SEQ"  MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE ;

create or replace 
TRIGGER SENT_AFTN_TRG BEFORE INSERT ON SENT_AFTN 
FOR EACH ROW 
BEGIN
  <<COLUMN_SEQUENCES>>
  BEGIN
    IF :NEW.ID IS NULL THEN
      SELECT SENT_AFTN_SEQ.NEXTVAL INTO :NEW.ID FROM DUAL;
    END IF;
  END COLUMN_SEQUENCES;
END;
