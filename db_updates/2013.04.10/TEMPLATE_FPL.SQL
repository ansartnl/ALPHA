CREATE TABLE TEMPLATE_FPL 
(
  ID NUMBER NOT NULL 
, NAME VARCHAR2(40 CHAR) NOT NULL 
, AIRCRAFT VARCHAR2(7 CHAR) 
, TAIRCRAFT VARCHAR2(4 CHAR) 
, FRULES VARCHAR2(1 CHAR) 
, TFLIGHT VARCHAR2(1 CHAR) 
, NUM NUMBER 
, WTC VARCHAR2(1 CHAR) 
, EQUIPMENT VARCHAR2(40 CHAR) 
, ADEP VARCHAR2(4 CHAR) 
, DEPARTURE_TIME TIMESTAMP 
, SPEED VARCHAR2(5 CHAR) 
, FLEVEL VARCHAR2(5 CHAR) 
, ROUTE VARCHAR2(4000 CHAR) 
, ADES VARCHAR2(4 CHAR) 
, EET VARCHAR2(5 CHAR) 
, ALT1 VARCHAR2(4 CHAR) 
, ALT2 VARCHAR2(4 CHAR) 
, OTHER VARCHAR2(4000 CHAR) 
, CONSTRAINT TEMPLATE_FPL_PK PRIMARY KEY 
  (
    ID 
  )
  ENABLE 
);

CREATE SEQUENCE  "AERO"."TEMPLATE_FPL_SEQ"  MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE ;

create or replace 
TRIGGER TEMPLATE_FPL_TRG BEFORE INSERT ON TEMPLATE_FPL 
FOR EACH ROW 
BEGIN
  <<COLUMN_SEQUENCES>>
  BEGIN
    IF :NEW.ID IS NULL THEN
      SELECT TEMPLATE_FPL_SEQ.NEXTVAL INTO :NEW.ID FROM DUAL;
    END IF;
  END COLUMN_SEQUENCES;
END;
