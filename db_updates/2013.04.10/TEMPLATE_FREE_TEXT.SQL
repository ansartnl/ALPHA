, TEXT VARCHAR2(4000 CHAR) NOT NULL 
, CONSTRAINT TEMPLATE_FREE_TEXT_PK PRIMARY KEY 
  (
    ID 
  )
  ENABLE 
);

CREATE SEQUENCE  "AERO"."TEMPLATE_FREE_TEXT_SEQ"  MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE ;

create or replace 
TRIGGER TEMPLATE_FREE_TEXT_TRG BEFORE INSERT ON TEMPLATE_FREE_TEXT 
FOR EACH ROW 
BEGIN
  <<COLUMN_SEQUENCES>>
  BEGIN
    IF :NEW.ID IS NULL THEN
      SELECT TEMPLATE_FREE_TEXT_SEQ.NEXTVAL INTO :NEW.ID FROM DUAL;
    END IF;
  END COLUMN_SEQUENCES;
END;
