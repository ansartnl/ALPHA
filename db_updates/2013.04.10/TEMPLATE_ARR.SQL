CREATE TABLE TEMPLATE_ARR 
(
  ID NUMBER NOT NULL 
, NAME VARCHAR2(40 CHAR) NOT NULL 
, AIRCRAFT VARCHAR2(7 CHAR) 
, ADEP VARCHAR2(4 CHAR) 
, ADES VARCHAR2(4 CHAR) 
, ARRIVAL_AIRPORT VARCHAR2(4 CHAR) 
, ARRIVAL_TIME TIMESTAMP 
, ARRIVAL_AIRPORT_NAME VARCHAR2(100 CHAR) 
, OTHER VARCHAR2(4000 CHAR) 
, CONSTRAINT TEMPLATE_ARR_PK PRIMARY KEY 
  (
    ID 
  )
  ENABLE 
);

CREATE SEQUENCE  "AERO"."TEMPLATE_ARR_SEQ"  MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE ;

create or replace 
TRIGGER TEMPLATE_ARR_TRG BEFORE INSERT ON TEMPLATE_ARR 
FOR EACH ROW 
BEGIN
  <<COLUMN_SEQUENCES>>
  BEGIN
    IF :NEW.ID IS NULL THEN
      SELECT TEMPLATE_ARR_SEQ.NEXTVAL INTO :NEW.ID FROM DUAL;
    END IF;
  END COLUMN_SEQUENCES;
END;
